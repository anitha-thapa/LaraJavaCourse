//switch cases or switch contion
class  A
{
	public static void main(String[] args) 
	{
		int i = 3;
		switch(i)
		{
			case 2:
				System.out.println("from case 2");
			case 5:
				System.out.println("from case 5");
			case 3:
				System.out.println("from case 3");
		}
	}
}

//from case 3

